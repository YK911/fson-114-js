function findMinNum(numbers) {
  let minNum = numbers[0];

  for (let i = 1; i < numbers.length; i++) {
    const currentNumber = numbers[i];

    if (currentNumber < minNum) {
      minNum = currentNumber;
    }
  }

  return minNum;
}

